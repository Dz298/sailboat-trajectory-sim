function [vx,vy] = polar_plot(true_wind_spd,p,x,y)
%{
% Calculate boat velocity rel. to water by wind and polar diagram
% INPUT:    
%   true_wind_spd: true wind speed [m/s]
%   p: struct created in main
%   x: current longitude
%   y: current latitude
% OUTPUT:
%   vx: boat speed rel. to water in x direction (along the local longitude axis)
%   vy: boat speed rel. to water in y direction (along the local latitude axis)

% Date: Oct. 29 2020
% Author: Daisy Zhang
%}

% for now, the boat is assumed to always travel to the destination at a 
% constant speed (0.1 m/s).
% Later the function should be able to calculate the boat's velocity given
% wind, and polar diagram generated by the other program. 
theta = atan2(p.des(2)-y,p.des(1)-x);
vx = 0.1*cos(theta);
vy = 0.1*sin(theta);
end